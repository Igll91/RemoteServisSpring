CREATE SCHEMA VESTIGO;

CREATE TABLE VESTIGO.MJESTO (
ID INT NOT NULL GENERATED ALWAYS AS IDENTITY,
PRIMARY KEY (ID),
NAZIV VARCHAR(100) NOT NULL
);

CREATE TABLE VESTIGO.PODACI (
ID INT NOT NULL GENERATED ALWAYS AS IDENTITY,
IME VARCHAR(100) NOT NULL,
PREZIME VARCHAR(100) NOT NULL,
TEL VARCHAR(100) NOT NULL,
MJESTO INT,
FOREIGN KEY (MJESTO) REFERENCES VESTIGO.MJESTO(ID),
PRIMARY KEY (ID)
);

